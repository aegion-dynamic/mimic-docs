    # =========================================================================
    # GENERATE ALL FILES
    # =========================================================================
    
    def generate_all(self, output_dir: str) -> dict:
        """Generate all code files and return as dictionary"""
        files = {
            'usart.h': self.generate_usart_h(),
            'usart.c': self.generate_usart_c(),
            'main_example.c': self.generate_main_example()
        }
        return files
    
    def save_all(self, output_dir: str):
        """Save all generated files to directory"""
        os.makedirs(output_dir, exist_ok=True)
        files = self.generate_all(output_dir)
        
        for filename, content in files.items():
            filepath = os.path.join(output_dir, filename)
            with open(filepath, 'w') as f:
                f.write(content)
            print(f"  ✓ Generated: {filepath}")


