def run_quick_generate(args):
    """Quick generation with command line arguments"""
    # Parse simple args
    config = UARTConfig()
    
    i = 0
    while i < len(args):
        arg = args[i]
        if arg in ['--uart', '-u'] and i + 1 < len(args):
            config.uart_instance = args[i + 1].upper()
            i += 2
        elif arg in ['--baud', '-b'] and i + 1 < len(args):
            config.baud_rate = int(args[i + 1])
            i += 2
        elif arg in ['--parity', '-p'] and i + 1 < len(args):
            p = args[i + 1].upper()
            config.parity = {'NONE': Parity.NONE, 'EVEN': Parity.EVEN, 'ODD': Parity.ODD}.get(p, Parity.NONE)
            i += 2
        elif arg in ['--stop', '-s'] and i + 1 < len(args):
            config.stop_bits = StopBits.TWO if args[i + 1] == '2' else StopBits.ONE
            i += 2
        elif arg in ['--frame', '-f'] and i + 1 < len(args):
            f = args[i + 1].upper()
            config.frame_type = {'TEXT': FrameType.TEXT, 'COMMAND': FrameType.COMMAND, 'BINARY': FrameType.BINARY}.get(f, FrameType.TEXT)
            i += 2
        elif arg in ['--output', '-o'] and i + 1 < len(args):
            output_dir = args[i + 1]
            i += 2
        elif arg in ['--help', '-h']:
            print_usage()
            return
        else:
            i += 1
    
    output_dir = output_dir if 'output_dir' in dir() else './generated_uart'
    
    print(f"Generating UART code for {config.uart_instance} @ {config.baud_rate} baud...")
    generator = UARTCodeGenerator(config)
    generator.save_all(output_dir)
    print(f"Done! Files saved to {output_dir}")


