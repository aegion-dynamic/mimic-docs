def get_choice(prompt: str, options: list, default: int = 0) -> int:
    """Get user choice from a list of options"""
    print(f"\n{prompt}")
    for i, opt in enumerate(options):
        marker = " [default]" if i == default else ""
        print(f"  {i + 1}. {opt}{marker}")
    
    while True:
        try:
            choice = input(f"\nEnter choice (1-{len(options)}) [default={default+1}]: ").strip()
            if not choice:
                return default
            idx = int(choice) - 1
            if 0 <= idx < len(options):
                return idx
            print(f"Please enter a number between 1 and {len(options)}")
        except ValueError:
            print("Invalid input. Please enter a number.")


def get_int(prompt: str, default: int, min_val: int = 0, max_val: int = 10000000) -> int:
    """Get integer input from user"""
    while True:
        try:
            value = input(f"{prompt} [default={default}]: ").strip()
            if not value:
                return default
            val = int(value)
            if min_val <= val <= max_val:
                return val
            print(f"Please enter a value between {min_val} and {max_val}")
        except ValueError:
            print("Invalid input. Please enter a number.")


def get_string(prompt: str, default: str) -> str:
    """Get string input from user"""
    value = input(f"{prompt} [default={default}]: ").strip()
    return value if value else default


